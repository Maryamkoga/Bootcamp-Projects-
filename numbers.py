# Numbers and arithmetic operations
print(5 + 3) # Addition
print(10 - 4)  # Subtraction
print(6 * 7)  # Multiplication
print(15 / 3)  # Division
print(2 ** 8)  # Exponentiation
print(10 % 3)  # Modulus
print(10 // 3)  # Floor division
# Order of operations
print(2 + 3 * 4)  # Multiplication before addition
print((2 + 3) * 4)  # Parentheses change the order



